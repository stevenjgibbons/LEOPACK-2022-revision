C*********************************************************************
C subroutine LONGitude in DEGrees 2 PHI (in radians) ******************
C            ----         ---     - ---              ******************
C Steve Gibbons Mon Mar 12 11:26:32 MET 2001                         C
C____________________________________________________________________C
C                                                                    C
C Input variables :-                                                 C
C ===============                                                    C
C                                                                    C
C  Real                                                              C
C  ----                                                              C
C                                                                    C
C     LONGDEG   : Longitude in degrees                               C
C     PHI       : Longitude in radians                               C
C                                                                    C
C____________________________________________________________________C
C
C*********************************************************************
      SUBROUTINE LONGDEG_2_PHI( LONGDEG, PHI )
      IMPLICIT NONE
C____________________________________________________________________C
C Variable declarations - Parameters ................................C
      REAL    LONGDEG, PHI
C____________________________________________________________________C
C Variable declarations - Working variables .........................C
      REAL    PI
      PARAMETER ( PI=3.1415926 )
C_____________________________________________________________________
C                  **************************************************C
C START OF PROGRAM **************************************************C
C____________________________________________________________________C
C
      PHI = PI*LONGDEG/180.0
C     .
      RETURN
      END
