C*********************************************************************
C function ODD or EVeN ? *********************************************
C            ---    -- -   *******************************************
C Steve Gibbons 12.5.97                                              C
C____________________________________________________________________C
C Takes an integer N and if N is even then the logical variable OE   C
C is set to .TRUE. and if it is odd then OE is set to .FALSE.        C
C____________________________________________________________________C
C
C*********************************************************************
      FUNCTION ODDEVN( N)
      IMPLICIT NONE
C____________________________________________________________________C
C Variable declarations - Parameters ................................C
      INTEGER N
      LOGICAL ODDEVN
C____________________________________________________________________C
C START OF PROGRAM **************************************************C
C____________________________________________________________________C
C 
      IF ( MOD( N, 2).EQ.0 ) THEN
         ODDEVN = .TRUE.
      ELSE
         ODDEVN = .FALSE.
      ENDIF
      RETURN
      END
      
